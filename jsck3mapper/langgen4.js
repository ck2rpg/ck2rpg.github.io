function capitalize(word) {
    if (!word) return '';
    return word.charAt(0).toUpperCase() + word.slice(1);
}

function pickFrom(arr) {
    if (!arr || !arr.length) return '';
    return arr[getRandomInt(0, arr.length - 1)];
}

function getRandomInt(min, max) {
    // inclusive on both sides
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

// tiny deterministic hash (you can use per-culture if you want determinism)
function hashString(str) {
  let h = 2166136261 >>> 0;
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

// Weighted picker
function weightedPick(arr, weightFn) {
    if (!arr || !arr.length) return null;
    let total = 0;
    const weights = new Array(arr.length);
    for (let i = 0; i < arr.length; i++) {
        const w = Math.max(weightFn(arr[i], i) || 0, 0.0001);
        weights[i] = w;
        total += w;
    }
    let r = Math.random() * total;
    for (let i = 0; i < arr.length; i++) {
        r -= weights[i];
        if (r <= 0) return arr[i];
    }
    return arr[arr.length - 1];
}

// Make raw words look less glitchy
function tidyWord(raw) {
    if (!raw) return '';

    let w = raw.toLowerCase();

    // Normalize some Lygia-derived roots a bit
    // "ygia" at the start -> "lygia" (or "igia" if you want)
    w = w.replace(/^ygia/, 'lygia');
    w = w.replace(/^ygi([aeiou])/, 'lygi$1');

    // Fix the exact "rsus" artifact to "ursus"
    if (w === 'rsus') w = 'ursus';

    // Collapse 3+ identical letters to 2
    w = w.replace(/([a-z])\1{2,}/g, '$1$1');

    // Tone down ultra-rare doubles that look alien
    w = w.replace(/([qxwjy])\1/g, '$1');

    // Leading double consonant like "ccelia" -> "celia"
    // but keep common double-l, double-r, double-s
    w = w.replace(
        /^([bcdfghjkmnpqtvwxz])\1/,
        '$1'
    );

    // q almost always followed by u
    w = w.replace(/q(?!u)/g, 'qu');

    // Avoid lone "q" at end
    w = w.replace(/q$/g, 'k');

    // Clean some "too weird" internal triples like bdv, ggrr, etc.
    w = w.replace(/bdv/g, 'biv');
    w = w.replace(/ggrr/g, 'gr');
    w = w.replace(/rgrr/g, 'rgr');

    // Trim stray h at very end after cluster
    w = w.replace(/h([b-df-hj-np-tv-z])$/g, '$1');

    return w;
}


