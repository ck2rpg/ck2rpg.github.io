function reduceClustersToSets(clusterObject) {
    const initial = new Set(
        (clusterObject.initialConsonants || clusterObject.initialVowels || [])
            .map(c => c.cluster)
    );
    const medial = new Set(
        (clusterObject.medialConsonants || clusterObject.medialVowels || [])
            .map(c => c.cluster)
    );
    const final = new Set(
        (clusterObject.finalConsonants || clusterObject.finalVowels || [])
            .map(c => c.cluster)
    );
    const initialSet = Array.from(initial);
    const medialSet  = Array.from(medial);
    const finalSet   = Array.from(final);
    return { initialSet, medialSet, finalSet };
}

