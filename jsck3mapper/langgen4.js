// ================== MARKOV MODEL (BIG REALISM BOOST) ==================

function buildMarkovModel(text, order = 2) {
    text = (text || '').toLowerCase();
    const words = text.match(/[a-z]+/g) || [];
    const transitions = {};
    const starts = [];

    words.forEach(word => {
        if (word.length < 2) return;
        const padded = '^' + word + '$';
        for (let i = 0; i + order < padded.length; i++) {
            const key = padded.slice(i, i + order);
            const next = padded[i + order];
            if (i === 0) starts.push(key);
            if (!transitions[key]) transitions[key] = {};
            transitions[key][next] = (transitions[key][next] || 0) + 1;
        }
    });

    return { order, transitions, starts };
}

function generateMarkovWord(model, minLen, maxLen) {
    if (!model || !model.starts.length) return '';

    const { order, transitions, starts } = model;

    // try a few times before giving up
    for (let attempt = 0; attempt < 5; attempt++) {
        let key = pickFrom(starts);   // e.g. "^m"
        if (!key) return '';

        // emit the first real letter from the start key
        let firstReal = key.replace('^', '')[0] || '';
        let out = firstReal;

        for (let steps = 0; steps < 64; steps++) {
            const nextMap = transitions[key];
            if (!nextMap) break;

            let chars = Object.keys(nextMap);
            if (!chars.length) break;

            // If we're too short, don’t allow ending
            if (out.length < minLen) {
                const filtered = chars.filter(ch => ch !== '$');
                if (filtered.length) chars = filtered;
            }

            // weighted pick
            let total = 0;
            for (let i = 0; i < chars.length; i++) {
                total += nextMap[chars[i]];
            }
            let r = Math.random() * total;
            let chosen = chars[chars.length - 1];
            for (let i = 0; i < chars.length; i++) {
                r -= nextMap[chars[i]];
                if (r <= 0) {
                    chosen = chars[i];
                    break;
                }
            }

            if (chosen === '$') break;

            out += chosen;
            if (out.length >= maxLen) break;

            key = key.slice(1) + chosen;
        }

        out = tidyWord(out);

        // --- quality gate ---

        const hasVowel = /[aeiou]/.test(out);
        const longConsonantRun = /[bcdfghjklmnpqrstvwxyz]{4,}/.test(out);

        // super short words with 2+ consonants are usually junk ("Bse", "Rsus")
        const shortAndWeird =
            out.length <= 3 && /[bcdfghjklmnpqrstvwxyz]{2,}/.test(out);

        if (out.length >= minLen && hasVowel && !longConsonantRun && !shortAndWeird) {
            return out;
        }
        // else: try another starting key
    }

    return '';
}



